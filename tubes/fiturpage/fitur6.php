                        Kota Bandung dikelilingi oleh pegunungan, sehingga bentuk morfologi wilayahnya bagaikan sebuah mangkuk raksasa,[11] secara geografis kota ini terletak di tengah-tengah provinsi Jawa Barat, serta berada pada ketinggian ±768 m di atas permukaan laut,[12] dengan titik tertinggi di berada di sebelah utara dengan ketinggian 1.050 meter [12] di atas permukaan laut dan sebelah selatan merupakan kawasan rendah dengan ketinggian 675 meter di atas permukaan laut.[12]
                        <br><br>
                        Kota Bandung dialiri dua sungai utama, yaitu Sungai Cikapundung dan Sungai Citarum beserta anak-anak sungainya yang pada umumnya mengalir ke arah selatan dan bertemu di Sungai Citarum. Dengan kondisi yang demikian, Bandung Selatan sangat rentan terhadap masalah banjir terutama pada musim hujan.[butuh pemastian]
                        <br><br>
                        Keadaan geologis dan tanah yang ada di kota Bandung dan sekitarnya terbentuk pada zaman kwartier dan mempunyai lapisan tanah alluvial hasil letusan Gunung Tangkuban Parahu.[12] Jenis material di bagian utara umumnya merupakan jenis andosol begitu juga pada kawasan dibagian tengah dan barat, sedangkan kawasan dibagian selatan serta timur terdiri atas sebaran jenis alluvial kelabu dengan bahan endapan tanah liat.[12]
                        <br><br>
                        Semetara iklim kota Bandung dipengaruhi oleh iklim pegunungan yang lembap dan sejuk, dengan suhu rata-rata 23,5 °C, curah hujan rata-rata 200,4mm dan jumlah hari hujan rata-rata 21,3 hari per bulan.[13]
                        <br><br>
                        Catatan dari Kepala Dinas Tata Ruang Kota Bandung, yakni Bambang Suhari, menyebutkan bahwa, di tahun 2022, terdapat 153 ribu m² lahan kuburan yang tersebar di setidaknya 13 TPU di Bandung.[14] 130 ribu m² di antaranya sudah terpakai sehingga tersisa 23 ribu m² sahaja.[14] Adapun dua kuburan yang masih dapat menampung layanan pemakaman baru meliputi TPU Cikadut dan TPU Nagrog.
